
public class CollegeStudent extends Student {
	int year;
	String stream;
	public void setYear(int year) {
		this.year=year;
	}
	public void setStream(String stream) {
		this.stream=stream;
	}
	public int getYear() {
		return year;	
	}
	public String getStream() {
		return stream;
	}
}
